const modalHtml = document.querySelector('#modal');
function openModal(titleText, bodyHtml, footerHtml) {
    const modal = new bootstrap.Modal(document.querySelector('#modal'));
    modalHtml.querySelector('.modal-title').innerText = titleText;
    modalHtml.querySelector('.modal-body').innerHTML = bodyHtml;
    modalHtml.querySelector('.modal-footer').innerHTML = footerHtml;
    modal.show();
}
function resetModal() {
    modalHtml.querySelector('.modal-title').innerText = '';
    modalHtml.querySelector('.modal-body').innerHTML = '';
    modalHtml.querySelector('.modal-footer').innerHTML = '';
    return 0;
}
